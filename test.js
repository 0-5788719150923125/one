function featherLayer(array, max = 1000) {
    let count = 0
    while (count < max) {
        for (let i = 1; i < array.length; i++) {
            if (array[i] < array[i - 1]) {
                const neuron = array[i]
                let j = i - 1
                while (j >= 0 && neuron < array[j]) {
                    array[j + 1] = array[j]
                    j--
                }
                array[j + 1] = neuron
                break
            }
        }
        count++
    }
    return array
}

